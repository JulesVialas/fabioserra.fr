            <h2 class="section-title">Chères Muretaines, chers Muretains, </h2>
            <p class="lettre-paragraphe">Chères Muretaines, chers Muretains,
                <b>J’aime Muret</b>, ville d’histoire et de mémoire, celle de la grande bataille de 1213, de Clément Ader et du premier avion, de Vincent Auriol maire puis président de la République, de Jacques Douzans bâtisseur de Muret.
                <br><br>
                Fils d’une aide-soignante et d’un ouvrier, c’est sur cette terre garonnaise que j’ai grandi et où j’étudie. <b>Juriste en formation</b>, passionné par la vie citoyenne, je me suis toujours engagé pour <b>préserver nos paysages</b> et <b>venir en aide aux plus vulnérables</b>.
                <br><br>
                Aujourd’hui, pour vous, <b>je suis candidat à l’élection municipale de Muret des 15 et 22 mars 2026</b>. Notre ville a besoin d’être gérée en bon père de famille. C’est pourquoi :
                <br><br>
                <b>Nous rétablirons l’ordre et la sécurité partout et pour tous</b> en luttant contre les trafics de drogue et la délinquance, <b>c’est notre priorité</b>.
                <br><br>
                <b>Nous veillerons à ce que Muret reste une ville à taille humaine</b> en stoppant la construction de logements sociaux et l’augmentation de la taxe foncière.
                <br><br>
                <b>Nous végétaliserons et nous retrouverons nos espaces verts</b> en mettant un terme à la bétonisation excessive et à la destruction de l’existant.
                <br><br>
                <b>Nous engagerons une baisse de la fiscalité locale</b> en luttant contre le matraquage fiscal qui pèse sur nos foyers.
                <br><br>
                <b>Nous nous assurerons de la propreté</b> en combattant les dépôts sauvages et les incivilités quotidiennes.
                <br><br>
                <b>Nous rétablirons l’attractivité de notre centre-ville</b> face aux grands commerces de périphérie.
                <br><br>
                <b>Nous encouragerons le développement du milieu associatif muretain</b> en mettant à leur disposition les moyens de la municipalité et en soutenant l’engagement bénévole.
                <br><br>
                En résumé, nous allons améliorer votre qualité de vie à Muret.
                <br><br>
                Depuis 2024, le Rassemblement National est le <b>premier parti de Muret</b>, en tête aux élections européennes et législatives. Nous sommes les seuls en capacité de <b>remporter l’élection municipale</b> de mars prochain et de <b>renouveler le paysage politique</b>.
                <br><br>
                <b>Ma candidature est celle de tous les patriotes et de toutes les droites muretaines</b>, celle de citoyens volontaires et unis dans l'intérêt des Muretains.
                <br><br>
                Pour soutenir la dynamique et retrouver Muret, j’ai besoin de vous. Participez à mon projet citoyen en cliquant <a href="#consultation-citoyenne">ici</a>.
                <br><br>
                Muretaines, Muretains, <b>ensemble retrouvons Muret</b>.
                <br><br>
                <h3 style="margin-bottom:2.5rem">Fabio Serra, candidat à la mairie de Muret</h3>
            </p>
